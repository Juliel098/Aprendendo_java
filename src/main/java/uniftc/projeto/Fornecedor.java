package uniftc.projeto;


public class Fornecedor {
    private String nome;
    private double valor_lona;
    private double qtd_lona;
    private double valor_tela;
    private double qtd_tela;
    private double metragem_rolo_tela;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getValor_lona() {
        return valor_lona;
    }

    public void setValor_lona(double valor_lona) {
        this.valor_lona = valor_lona;
    }

    public double getQtd_lona() {
        return qtd_lona;
    }

    public void setQtd_lona(double qtd_lona) {
        this.qtd_lona = qtd_lona;
    }

    public double getValor_tela() {
        return valor_tela;
    }

    public void setValor_tela(double valor_tela) {
        this.valor_tela = valor_tela;
    }

    public double getQtd_tela() {
        return qtd_tela;
    }

    public void setQtd_tela(double qtd_tela) {
        this.qtd_tela = qtd_tela;
    }

    public double getMetragem_rolo_tela() {
        return metragem_rolo_tela;
    }

    public void setMetragem_rolo_tela(double metragem_rolo_tela) {
        this.metragem_rolo_tela = metragem_rolo_tela;
    }
    
    
    
    
}
